function resetMap() {
  map.setView([30, 5], 1.5);

  if (marker) {
    map.removeLayer(marker);
    marker = null;
  }

  markerPlaced = false;

  document.body.style.cursor = "none";
  cursor.style.display = "block";

  polygonche = null;

  document.addEventListener("mousemove", (e) => {
    if (!markerPlaced) {
      cursor.style.left = `${e.clientX}px`;
      cursor.style.top = `${e.clientY}px`;
    }
  });
}

