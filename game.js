function timer() {
  if (ostavashtovreme > 0) {
    ostavashtovreme--;
    setTimeout(timer, 1000);
    timeLeft = 30;
  } else {
    closeModal();
    ostavashtovreme = 5;
    timeLeft = 30;
  }
}

function continueGame() {
  if (round < maxRounds) {
    if (!markerPlaced) {
      alert("Please put a marker on the map, sir!");
      return;
    }
    createShapeche(currentLocation);
    round++;
    setRandomLocation();
    checkGuess();
    resetMap();
    updateUI();

    timer();
    timeLeft = 30;
    countdown();
    console.log("countdown starts!");
  } else {
    endGame();
  }
}

