function setRandomLocation() {
  if (usedLocations.size >= location_names.length) {
    alert("No more unique locations available! Restarting the game.");
    startGame();
    return;
  }

  let randomIndex;
  let attempts = 0;
  do {
    randomIndex = Math.floor(Math.random() * location_names.length);
    attempts++;
    if (attempts > 100) {
      console.error("Failed to find a unique location after 100 attempts.");
      return;
    }
  } while (usedLocations.has(randomIndex));

  console.log("Selected randomIndex:", randomIndex);

  usedLocations.add(randomIndex);
  currentLocation = location_names[randomIndex];

  console.log("Selected location:", currentLocation);

  if (!currentLocation) {
    console.error("Error: currentLocation is undefined! Check location_names.");
    return;
  }

  document.getElementById("random").textContent = currentLocation;

  console.log("Current location before:", currentLocation);

  createShape(currentLocation);
  console.log(
    "Marker coordinates:",
    marker ? marker.getLatLng() : "No marker yet."
  );
}

function isMarkerInsidePolygon() {
  let markerLatLng = marker.getLatLng();
  let latlngs = polygonche.getLatLngs()[0];

  let coordinates = latlngs.map((coord) => [coord.lng, coord.lat]);

  if (
    coordinates[0][0] !== coordinates[coordinates.length - 1][0] ||
    coordinates[0][1] !== coordinates[coordinates.length - 1][1]
  ) {
    coordinates.push(coordinates[0]);

    let markerGeoJSON = turf.point([markerLatLng.lng, markerLatLng.lat]);
    let polygonGeoJSON = turf.polygon([coordinates]);

    return turf.booleanPointInPolygon(markerGeoJSON, polygonGeoJSON);
  }
}

function checkGuess(currentLocation) {
  if (!marker) {
    console.log("Error: Marker is not placed yet.");
    return;
  }

  console.log("Current location:", currentLocation);
  console.log(
    "Chosen location coordinates:",
    location_coordinates[currentLocation]
  );

  let markercoords = marker.getLatLng();
  let chosenLocationCoords = location_coordinates[currentLocation];

  if (!chosenLocationCoords) {
    console.log("Error: Chosen location not found.");
    return;
  }

  if (isMarkerInsidePolygon()) {
    tochkizarund = 5000;
    points += 5000;
  } else {
    let markerGeoJSON = turf.point([markercoords.lng, markercoords.lat]);

    let polygonGeoJSON = polygonche.toGeoJSON();
    if (!polygonGeoJSON || !polygonGeoJSON.geometry) {
      console.log("Error: Polygon is not valid GeoJSON.");
      return;
    }

    let nearestPoint = turf.nearestPointOnLine(polygonGeoJSON, markerGeoJSON);

    if (
      !nearestPoint ||
      !nearestPoint.geometry ||
      !nearestPoint.geometry.coordinates
    ) {
      console.log("Error: Nearest point calculation failed.");
      return;
    }

    let nearestLat = nearestPoint.geometry.coordinates[1];
    let nearestLng = nearestPoint.geometry.coordinates[0];

    if (nearestLat === undefined || nearestLng === undefined) {
      console.log("Error: Nearest point coordinates are undefined.");
      return;
    }

    let distance = markercoords.distanceTo(L.latLng(nearestLat, nearestLng));

    if (isNaN(distance)) {
      console.log("Error: Distance calculation failed.");
      return;
    }

    console.log(`Distance: ${distance} meters`);

    let score = Math.max(5000 - 0.1 * distance, 0);

    if (isNaN(score)) {
      console.log("Error: Score calculation resulted in NaN.");
      return;
    }

    console.log(`Score: ${score}`);

    points += score;
    tochkizarund = score;
  }

  updateUI();
}

